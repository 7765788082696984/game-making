from turtle import *
speed(5)
def rect(color):
    begin_fill()
    fillcolor(color)
    for i in range(2):
        fd(400)
        rt(90)
        fd(100)
        rt(90)
    end_fill()

screensize(1100,1100)
up()
pensize(10)
goto(0,-300)
down()
goto(0,400)
pensize(4)
rect("orange")
goto(0,300)
fd(200)
color('blue')
circle(-50)
setheading(270)
fd(50)
setheading(0)
for i in range(24):
    fd(45)
    bk(45)
    lt(15)
setheading(90)
fd(50)
setheading(0)
color('black')
fd(200)
rt(90)
fd(100)
rt(90)
fd(400)
rt(90)
fd(100)
rt(90)
goto(0,200)
rect('green')
rt(90)
fd(250)
lt(90)
penup()
fd(30)
pendown()
hideturtle()
color("red")
write('our national flag',font=('arial',16,'normal'))
done()